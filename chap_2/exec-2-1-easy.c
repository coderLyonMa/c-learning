#include <stdio.h>
#include <limits.h>

int main()
{
    printf("Maximum value of int is : %d\n", INT_MAX);
    printf("Minimum value of int is : %d\n", INT_MIN);
    printf("Maximum value of unsigned int is : %d\n", UINT_MAX);
    printf("Minimum value of unsigned int is : 0\n");
    printf("Maximum value of char is :%d\n", CHAR_MAX);
    printf("Minimum value of char is :%d\n", CHAR_MIN);
    printf("Minimum value of unsigned char is :%d\n", UCHAR_MAX);
    printf("Minimum value of unsigned char is 0\n");
    printf("Maximum value of short int is :%d\n", SHRT_MAX);
    printf("Minimum value of short int is :%d\n", SHRT_MIN);
    printf("Maximum value of unsigned short int is %d\n", USHRT_MAX);
    printf("Minimum value of unsigned short int is 0\n");
    printf("Maximum value of long int is %ld\n", LONG_MAX);
    printf("Maximum value of long int is %ld\n", LONG_MIN);
    printf("Maximum value of unsigned long int is %ld\n", ULONG_MAX);
    printf("Minimum value of unsigned long int is 0\n");


}
